package class_13_ForLoopNeverStop_ScanAllTheMethods;

public class Class_13_Print_EvenNumber 
{
	public static void main(String[] args) 
	{
		for (int i=1;i<100;i++)
		{
			if(i%2==0)
			{
				System.out.println("This is even"+i);
			}
			else
			{
				System.out.println("This is odd"+i);
			}
		}
		
		
	}

}
