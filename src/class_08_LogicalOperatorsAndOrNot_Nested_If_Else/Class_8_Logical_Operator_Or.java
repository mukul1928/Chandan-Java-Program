package class_08_LogicalOperatorsAndOrNot_Nested_If_Else;

public class Class_8_Logical_Operator_Or 
{
	public static void main(String[] args) 
	{
		int age=19;
		char gender='M';
		if(age>18 || gender=='C')
		{
		System.out.println("I am eligible to vote");	
		}
		else
		{
		System.out.println("I am not eligible to vote");
		}
	}

}
